my_str ='meghalaya'

print(f'My String={my_str}') # print the string  as it is
print(f'My String={my_str[::]}') # start from index 0 till last possible index of the  string
print(f'My String={my_str[1:8:]}') # start from index 1 till 7 index of the  string
print(f'My String={my_str[1:8:2]}') # start from index 1 till  index 7 and jump 2 steps of the  string


